package Staff;

public class Sigara {
    private  int Lenght;
    private int Price;
    private   String Name;
    public Sigara(int Lenght,int Price,String  Name){
        this.Lenght= Lenght;
        this.Price=Price;
        this.Name=Name;

    }
    public  int getLenght(){
        return Lenght;
    }
    public  int getPrice(){
        return Price;
    }
    public  String getName(){
        return Name;
    }



    }




